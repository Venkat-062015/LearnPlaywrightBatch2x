// Identifier case style examples

var name = "Venkat";

// camelCase: common in JavaScript for variables and functions
var firstName = "Venkat";
var lastName = "Rao";
var totalAmount = 100;

// snake_case: often used in some naming styles
var first_name = "Venkat";
var last_name = "Rao";
var total_amount = 100;

// PascalCase: used for constructor functions and classes
var EmployeeName = "Venkat";
var UserAccount = "admin";

// UPPER_SNAKE_CASE: used for constants
var MAX_VALUE = 1000;
var API_KEY = "ABC123";

// Note: kebab-case is not valid for JavaScript identifiers because the hyphen is interpreted as subtraction
// var user-name = "Venkat"; // invalid

console.log(name, firstName, lastName, first_name, last_name, EmployeeName, UserAccount, MAX_VALUE, API_KEY);

